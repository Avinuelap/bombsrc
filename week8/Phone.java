package week8;
/**
 * This represents a phone:
 * y=year,
 * b=brand,
 * m=model,
 * @author Antonio Viñuela
 */

public class Phone {
	public int year;
	public String brand;
	public String model;

	public Phone() {}
	
	 public Phone (int y, String b, String m) {
		year = y;
		brand = b;
		model = m;

	}
}